function completeMission(score) {
  const mission = getMission();
  state.missionRunComplete = true;
  renderBuilder();
  if (isStudyMode()) {
    const missionIndex = MISSION_KEYS.indexOf(mission.key);
    const nextMissionKey = MISSION_KEYS[missionIndex + 1] || null;
    renderStats();
    setMissionStep(2);
    showFeedback({
      success: true,
      score,
      title: mission.successTitle,
      copy: `${mission.successCopy} Это учебный запуск: основной прогресс и награды не изменены.`,
      action: nextMissionKey ? `Перейти к заданию ${missions[nextMissionKey].number}` : "Вернуться на карту",
      actionType: nextMissionKey ? `next-${nextMissionKey}` : "map"
    });
    return;
  }
  const firstCompletion = !state[mission.completionFlag];
  state[mission.completionFlag] = true;
  state.energy = Math.min(3, state.energy + 1);

  if (firstCompletion) {
    if (mission.key === "interface") {
      state.xp = Math.min(55, state.xp + score);
    } else if (mission.key === "data") {
      state.xp = 100;
    } else if (mission.key === "access") {
      state.xp = 150;
    } else if (mission.key === "process") {
      state.xp = 205;
    } else if (mission.key === "case") {
      state.xp = 250;
    } else if (mission.key === "integration") {
      state.xp = 350;
    } else if (mission.key === "insight") {
      state.xp = 400;
    } else if (mission.key === "classification") {
      state.xp = 450;
    } else if (mission.key === "solution") {
      state.xp = 500;
    }
  }

  state.level = state.caseMissionComplete ? 2 : 1;
  saveState();
  renderStats();
  renderMapState();
  setMissionStep(2);

  const nextMissionKey = mission.key === "interface" && !state.dataMissionComplete
    ? "data"
    : mission.key === "data" && !state.accessMissionComplete
      ? "access"
      : mission.key === "access" && !state.processMissionComplete
        ? "process"
        : mission.key === "process" && !state.caseMissionComplete
          ? "case"
          : mission.key === "case" && !state.integrationMissionComplete
            ? "integration"
          : mission.key === "integration" && !state.insightMissionComplete
            ? "insight"
          : mission.key === "insight" && !state.classificationMissionComplete
            ? "classification"
          : mission.key === "classification" && !state.solutionMissionComplete
            ? "solution"
      : null;
  const finalMission = mission.key === "solution";
  showFeedback({
    success: true,
    score: firstCompletion ? score : 0,
    title: mission.successTitle,
    copy: firstCompletion ? mission.successCopy : mission.replayCopy,
    action: finalMission ? "Завершить карту" : nextMissionKey ? `Перейти к заданию ${missions[nextMissionKey].number}` : "Вернуться на карту",
    actionType: finalMission ? "finale" : nextMissionKey ? `next-${nextMissionKey}` : "map"
  });
}

function failAttempt(result) {
  const mission = getMission();
  const firstBlueprintError = mission.mode === "blueprint"
    ? state.selected.findIndex((id, index) => id !== mission.correct[index])
    : -1;
  const firstClassificationError = mission.mode === "classification"
    ? state.selected.findIndex((id, index) => id !== mission.correct[index])
    : -1;
  state.energy = Math.max(0, state.energy - 1);
  state.attempts += 1;

  if (mission.mode === "blueprint") {
    state.blueprintLocked = state.selected.map((id, index) => id === mission.correct[index]);
    state.selected = state.selected.map((id, index) => state.blueprintLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.blueprintLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.blueprintComplete = false;
    renderBuilder();
  } else if (mission.mode === "classification") {
    state.classificationLocked = state.selected.map((id, index) => id === mission.correct[index]);
    state.selected = state.selected.map((id, index) => state.classificationLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.classificationLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.classificationComplete = false;
    renderBuilder();
  } else if (mission.sceneInteraction === "forge") {
    state.forgeLocked = state.selected.map((id, index) => id === mission.correct[index]);
    state.selected = state.selected.map((id, index) => state.forgeLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.forgeLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.forgeComplete = false;
    renderBuilder();
  } else if (mission.sceneInteraction === "engine") {
    state.engineLocked = state.selected.map((id, index) => id === mission.correct[index]);
    state.selected = state.selected.map((id, index) => state.engineLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.engineLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.engineComplete = false;
    renderBuilder();
  } else if (["arena", "harbor"].includes(mission.sceneInteraction)) {
    const kind = mission.sceneInteraction;
    state[`${kind}Locked`] = state.selected.map((id, index) => id === mission.correct[index]);
    state.selected = state.selected.map((id, index) => state[`${kind}Locked`][index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state[`${kind}Locked`][index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state[`${kind}Complete`] = false;
    renderBuilder();
  }

  saveState();
  renderStats();
  setMissionStep(1);

  let clue;
  if (mission.mode === "blueprint") {
    const node = mission.nodes[firstBlueprintError];
    clue = node
      ? `${node.explanation} ${node.trap}`
      : "Сверьте границы ответственности шести систем решения.";
  } else if (mission.mode === "classification") {
    const requirement = mission.requirements[firstClassificationError];
    clue = requirement
      ? `${requirement.explanation} ${requirement.trap}`
      : "Сверьте границу между готовой возможностью, настройкой, внешним контрактом и собственным кодом.";
  } else if (mission.sceneInteraction === "forge") {
    clue = result.traps > 0
      ? "Неподходящие термины вернулись в список. Подтверждённые определения закреплены — продолжите с активного пункта."
      : "Верные термины закреплены, а перепутанные вернулись в список. Сопоставьте назначение конструкции с её местом в модели данных.";
  } else if (mission.sceneInteraction === "engine") {
    clue = result.traps > 0
      ? "Неподходящие элементы удалены из схемы. Подтверждённые шаги закреплены — продолжите с активного пункта."
      : "Нужные элементы выбраны, но часть назначена не тем шагам. Сверьте путь: событие, условие, чтение, создание записи и завершение.";
  } else if (mission.key === "insight") {
    clue = result.traps > 0
      ? "На панель попал инструмент не для оперативной аналитики. Сначала определите, нужен ответ числом, графиком, списком, матрицей или шкалой."
      : "Все нужные дашборды выбраны, но назначены не тем вопросам. Сравните формат управленческого ответа с видом визуализации.";
  } else if (mission.key === "integration") {
    clue = result.traps > 0
      ? "Одна из частей контракта настроена механизмом с другим направлением, уровнем безопасности или режимом обработки. Сверьте инициатора вызова и ожидаемый ответ."
      : "Нужные настройки выбраны, но назначены не тем частям. Разделите канал, доступ, контракт, обработку и ответ.";
  } else if (mission.key === "case") {
    clue = result.traps > 0
      ? "В настройку попал элемент, который не решает это правило жизненного цикла. Сопоставьте бизнес-требование с точным элементом или параметром стадии."
      : "Все нужные механизмы выбраны, но назначены не тем правилам. Разделите действия пользователя, автоматический переход и признак результата.";
  } else if (mission.mode === "matching") {
    clue = result.traps > 0
      ? "В ответ попал термин из другой области платформы. Здесь нужны только понятия модели данных."
      : "Все нужные термины найдены, но часть определений перепутана. Сравните, что хранит значение, а что связывает записи.";
  } else if (mission.key === "process") {
    clue = result.traps > 0
      ? "В схему попал механизм, который не является элементом этого бизнес-процесса. Здесь нужны событие, условия запуска и действия с данными."
      : "Все нужные элементы найдены, но порядок нарушен: сначала событие и его условия, затем чтение и создание данных, после этого завершение.";
  } else {
    clue = result.traps > 0
      ? "В цепочке есть термин другой природы. Здесь нужна только структура пользовательского интерфейса."
      : "Все нужные термины найдены, но порядок нарушен: начните с навигации и закончите связанными записями.";
  }

  showFeedback({
    success: false,
    score: result.score,
    title: mission.key === "insight"
      ? "Панель пока не отвечает задаче"
      : mission.mode === "blueprint"
      ? "Выпускное решение пока не проходит проверку"
      : mission.mode === "classification"
      ? "Архитектурный разбор пока неточен"
      : mission.sceneInteraction === "forge"
      ? "Словарь заполнен не полностью"
      : mission.sceneInteraction === "engine"
      ? "Процесс пока не проходит проверку"
      : mission.key === "integration"
      ? "Интеграция пока не проходит проверку"
      : mission.key === "case"
      ? "Кейс пока неустойчив"
      : mission.mode === "matching"
      ? "Словарь пока неточен"
      : mission.key === "process"
        ? "Схема пока не запускается"
        : "Путь пока нестабилен",
    copy: clue,
    action: "Исправить ответы",
    actionType: "retry"
  });
}

function showFeedback({ success, score, title, copy, action, actionType }) {
  const mission = getMission();
  elements.feedbackPanel.hidden = false;
  elements.feedbackPanel.classList.toggle("is-success", success);
  elements.feedbackPanel.classList.toggle("is-error", !success);
  elements.feedbackKicker.textContent = success ? "Победа" : "Подсказка профессора";
  elements.feedbackTitle.textContent = title;
  elements.feedbackCopy.textContent = copy;
  elements.scoreValue.textContent = score;
  elements.feedbackAction.textContent = action;
  elements.feedbackAction.dataset.action = actionType;
  elements.feedbackPath.innerHTML = mission.correct
    .map((id, index) => {
      const description = ["matching", "gateway"].includes(mission.mode)
        ? `<span>${explicitQuestionText(mission, mission.prompts[index])}</span>`
        : mission.mode === "blueprint"
          ? `<span>${explicitQuestionText(mission, mission.nodes[index].prompt)}</span>`
        : mission.mode === "classification"
          ? `<span>${explicitQuestionText(mission, mission.requirements[index].prompt)}</span>`
        : mission.mode === "quiz"
          ? `<span>${explicitQuestionText(mission, mission.questions[index].prompt)}</span>`
          : "";
      return `<li><strong>${getTool(id, mission).name}</strong>${description}</li>`;
    })
    .join("");
  elements.feedbackPanel.scrollIntoView({ behavior: "smooth", block: "nearest" });
}

