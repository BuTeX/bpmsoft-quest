function shuffleAnswers(answers, { correctId = null, correctIds = [] } = {}) {
  const shuffled = [...answers];

  for (let index = shuffled.length - 1; index > 0; index -= 1) {
    const target = Math.floor(Math.random() * (index + 1));
    [shuffled[index], shuffled[target]] = [shuffled[target], shuffled[index]];
  }

  const originalIds = answers.map(getAnswerId);
  const shuffledIds = shuffled.map(getAnswerId);
  if (shuffled.length > 1 && shuffledIds.every((id, index) => id === originalIds[index])) {
    [shuffled[0], shuffled[1]] = [shuffled[1], shuffled[0]];
  }

  if (correctId && shuffled.length > 1 && getAnswerId(shuffled[0]) === correctId) {
    const target = 1 + Math.floor(Math.random() * (shuffled.length - 1));
    [shuffled[0], shuffled[target]] = [shuffled[target], shuffled[0]];
  }

  if (correctIds.length > 0) {
    const correctSet = new Set(correctIds);
    const distractorIndex = shuffled.findIndex((answer) => !correctSet.has(getAnswerId(answer)));
    if (distractorIndex > 0 && correctSet.has(getAnswerId(shuffled[0]))) {
      [shuffled[0], shuffled[distractorIndex]] = [shuffled[distractorIndex], shuffled[0]];
    }

    const shuffledCorrectIds = shuffled
      .map(getAnswerId)
      .filter((id) => correctSet.has(id));
    const keepsCorrectOrder = shuffledCorrectIds.length > 1
      && shuffledCorrectIds.every((id, index) => id === correctIds[index]);

    if (keepsCorrectOrder) {
      const firstIndex = shuffled.findIndex((answer) => getAnswerId(answer) === correctIds[0]);
      const secondIndex = shuffled.findIndex((answer) => getAnswerId(answer) === correctIds[1]);
      [shuffled[firstIndex], shuffled[secondIndex]] = [shuffled[secondIndex], shuffled[firstIndex]];
    }
  }

  return shuffled;
}

function getShuffledAnswers(answers, key, constraints = {}) {
  const mission = getMission();
  const orderKey = `${mission.key}:${key}`;
  const availableById = new Map(answers.map((answer) => [getAnswerId(answer), answer]));
  const savedOrder = state.answerOrders[orderKey];
  const validSavedOrder = Array.isArray(savedOrder)
    && savedOrder.length === answers.length
    && savedOrder.every((id) => availableById.has(id));

  if (!validSavedOrder) {
    state.answerOrders[orderKey] = shuffleAnswers(answers, constraints).map(getAnswerId);
  }

  return state.answerOrders[orderKey].map((id) => availableById.get(id));
}

