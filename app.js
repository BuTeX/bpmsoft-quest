function clearBuild() {
  if (getMission().mode === "blueprint") {
    state.selected = state.selected.map((id, index) => state.blueprintLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.blueprintLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.blueprintComplete = false;
  } else if (getMission().mode === "classification") {
    state.selected = state.selected.map((id, index) => state.classificationLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.classificationLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.classificationComplete = false;
  } else if (getMission().sceneInteraction === "forge") {
    state.selected = state.selected.map((id, index) => state.forgeLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.forgeLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.forgeComplete = false;
  } else if (getMission().sceneInteraction === "engine") {
    state.selected = state.selected.map((id, index) => state.engineLocked[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !state.engineLocked[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state.engineComplete = false;
  } else if (["arena", "harbor"].includes(getMission().sceneInteraction)) {
    const kind = getMission().sceneInteraction;
    const lockedStations = state[`${kind}Locked`];
    state.selected = state.selected.map((id, index) => lockedStations[index] ? id : null);
    const nextEmpty = state.selected.findIndex((value, index) => !value && !lockedStations[index]);
    state.activeSlot = nextEmpty >= 0 ? nextEmpty : 0;
    state[`${kind}Complete`] = false;
  } else {
    state.selected = getMission().mode === "sequence" ? [] : Array(getMission().correct.length).fill(null);
    state.activeSlot = 0;
  }
  hideFeedback();
  renderBuilder();
}

function calculateScore() {
  const mission = getMission();
  const selectedIds = state.selected.filter(Boolean);
  const exactMatches = state.selected.filter((id, index) => id === mission.correct[index]).length;
  const correctTools = selectedIds.filter((id) => mission.correct.includes(id)).length;
  const traps = selectedIds.length - correctTools;

  if (mission.mode === "classification") {
    const wrongAssignments = selectedIds.length - exactMatches;
    const score = exactMatches * 4 + (exactMatches === mission.correct.length ? 2 : 0);
    return { exactMatches, correctTools: exactMatches, traps: wrongAssignments, score };
  }

  if (mission.mode === "blueprint") {
    const wrongAssignments = selectedIds.length - exactMatches;
    const score = exactMatches * 8 + (exactMatches === mission.correct.length ? 2 : 0);
    return { exactMatches, correctTools: exactMatches, traps: wrongAssignments, score };
  }

  if (mission.mode === "gateway") {
    const score = exactMatches * 15 + (exactMatches === mission.correct.length ? 25 : 0);
    return { exactMatches, correctTools, traps, score };
  }

  if (mission.mode === "matching") {
    const score = exactMatches * 7 + (exactMatches === mission.correct.length ? 10 : 0);
    return { exactMatches, correctTools, traps, score };
  }

  const misplacedTools = correctTools - exactMatches;
  const orderedBonus = exactMatches === mission.correct.length ? 10 : 0;
  const terminologyBonus = traps === 0 ? 5 : 0;
  const score = Math.max(0, exactMatches * 8 + misplacedTools * 3 + orderedBonus + terminologyBonus);
  return { exactMatches, correctTools, traps, score };
}

function checkSolution() {
  if (state.missionRunComplete) return false;
  const requiredCount = getMission().correct.length;
  if (selectedCount() < requiredCount) {
    elements.missionHint.textContent = getMission().mode === "blueprint"
      ? `Сначала назначьте модули всем шести частям решения. Заполнено: ${selectedCount()} из ${requiredCount}.`
      : getMission().mode === "classification"
      ? `Сначала назначьте категории всем 12 требованиям. Заполнено: ${selectedCount()} из ${requiredCount}.`
      : getMission().sceneInteraction === "forge"
      ? "Сначала назначьте термины всем пяти определениям."
      : getMission().sceneInteraction === "engine"
        ? "Сначала установите элементы на все пять шагов процесса."
      : getMission().sceneInteraction === "arena"
        ? "Сначала назначьте механизмы всем пяти правилам DCM-кейса."
      : getMission().sceneInteraction === "harbor"
        ? "Сначала настройте все пять частей интеграционного контракта."
      : "Для проверки заполните все пять карточек.";
    elements.missionHint.style.color = "var(--rust)";
    return;
  }

  elements.missionHint.style.color = "";
  const result = calculateScore();
  const success = result.exactMatches === getMission().correct.length;

  if (success) {
    if (getMission().mode === "blueprint") {
      state.blueprintLocked = Array(getMission().correct.length).fill(true);
      startBlueprintTests();
    } else if (getMission().mode === "gateway") {
      if (getMission().sceneInteraction === "harbor") {
        state.harborLocked = Array(5).fill(true);
        state.harborComplete = true;
      }
      startGatewayTests();
    } else {
      if (getMission().sceneInteraction === "forge") {
        state.forgeLocked = Array(5).fill(true);
        state.forgeComplete = true;
        renderBuilder();
      }
      if (getMission().sceneInteraction === "engine") {
        state.engineLocked = Array(5).fill(true);
        state.engineComplete = true;
        renderBuilder();
      }
      if (getMission().sceneInteraction === "arena") {
        state.arenaLocked = Array(5).fill(true);
        state.arenaComplete = true;
        renderBuilder();
      }
      if (getMission().mode === "classification") {
        state.classificationLocked = Array(getMission().correct.length).fill(true);
        state.classificationComplete = true;
        renderBuilder();
      }
      completeMission(result.score);
    }
  } else {
    failAttempt(result);
  }
}

