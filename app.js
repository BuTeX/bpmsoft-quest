function assignMatchingTerm(id) {
  const targetIndex = state.activeSlot;
  const existingIndex = state.selected.indexOf(id);

  if (existingIndex === targetIndex) {
    state.selected[targetIndex] = null;
    return;
  }

  const displacedTerm = state.selected[targetIndex] || null;
  if (existingIndex >= 0) {
    state.selected[existingIndex] = displacedTerm;
  }
  state.selected[targetIndex] = id;

  const nextEmpty = state.selected.findIndex((value) => !value);
  state.activeSlot = nextEmpty >= 0 ? nextEmpty : (targetIndex + 1) % 5;
}

function assignForgeTerm(id) {
  const targetIndex = state.activeSlot;
  if (state.forgeLocked[targetIndex]) return;

  const existingIndex = state.selected.indexOf(id);
  if (existingIndex >= 0 && state.forgeLocked[existingIndex]) return;

  if (existingIndex === targetIndex) {
    state.selected[targetIndex] = null;
  } else {
    if (existingIndex >= 0) state.selected[existingIndex] = null;
    state.selected[targetIndex] = id;
  }

  const nextEmpty = state.selected.findIndex((value, index) => !value && !state.forgeLocked[index]);
  if (nextEmpty >= 0) {
    state.activeSlot = nextEmpty;
  } else {
    const nextEditable = state.forgeLocked.findIndex((locked) => !locked);
    state.activeSlot = nextEditable >= 0 ? nextEditable : targetIndex;
  }
}

