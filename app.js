function beginMission(key) {
  if (!isStudyMode()) {
    if (key === "data" && !state.missionComplete) return;
    if (key === "access" && !state.dataMissionComplete) return;
    if (key === "process" && !state.accessMissionComplete) return;
    if (key === "case" && !state.processMissionComplete) return;
    if (key === "integration" && !state.caseMissionComplete) return;
    if (key === "insight" && !state.integrationMissionComplete) return;
    if (key === "classification" && !state.insightMissionComplete) return;
    if (key === "solution" && !state.classificationMissionComplete) return;
  }

  state.activeMission = key;
  state.missionRunComplete = false;
  state.answerOrders = {};
  const usesImageStations = missions[key].sceneInteraction === "engine";
  state.selected = missions[key].mode === "sequence" && !usesImageStations
    ? []
    : Array(missions[key].correct.length).fill(null);
  state.activeSlot = 0;
  state.attempts = 1;
  state.gatewayBuilt = false;
  state.gatewayConfig = [];
  state.gatewayTestAnswers = [];
  state.forgeLocked = Array(5).fill(false);
  state.forgeComplete = false;
  state.accessWrongAnswer = null;
  state.accessComplete = false;
  state.engineLocked = Array(5).fill(false);
  state.engineComplete = false;
  state.arenaLocked = Array(5).fill(false);
  state.arenaComplete = false;
  state.harborLocked = Array(5).fill(false);
  state.harborComplete = false;
  state.insightWrongAnswer = null;
  state.insightComplete = false;
  state.classificationLocked = Array(12).fill(false);
  state.classificationComplete = false;
  state.blueprintBuilt = false;
  state.blueprintConfig = [];
  state.blueprintTestAnswers = [];
  state.blueprintLocked = Array(6).fill(false);
  state.blueprintComplete = false;
  renderMissionContent();
  hideFeedback();
  renderBuilder();
  setMissionStep(0);
  showView("mission");
  if (state.introSeen.includes(key)) hideMissionIntro();
  else openMissionIntro(missions[key]);
}

