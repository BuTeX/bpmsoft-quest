function toggleTool(id) {
  if (state.missionRunComplete) return;
  if (getMission().mode === "quiz") {
    answerQuiz(id);
    return;
  }

  if (getMission().sceneInteraction === "forge") {
    assignForgeTerm(id);
  } else if (getMission().sceneInteraction === "engine") {
    assignEngineModule(id);
  } else if (["arena", "harbor"].includes(getMission().sceneInteraction)) {
    assignNodeTool(getMission().sceneInteraction, id);
  } else if (["matching", "gateway"].includes(getMission().mode)) {
    assignMatchingTerm(id);
  } else {
    toggleSequenceTerm(id);
  }

  hideFeedback();
  renderBuilder();
}

function answerQuiz(id) {
  if (state.missionRunComplete) return;
  const mission = getMission();
  const questionIndex = state.activeSlot;
  const question = mission.questions[questionIndex];
  const citadel = mission.sceneInteraction === "citadel";
  const dialog = mission.sceneInteraction === "dialog";

  if (id !== question.correct) {
    state.energy = Math.max(0, state.energy - 1);
    state.attempts += 1;
    if (citadel) state.accessWrongAnswer = id;
    if (dialog) state.insightWrongAnswer = id;
    elements.missionHint.textContent = citadel
      ? `Вариант не подходит. ${question.explanation}`
      : dialog
        ? `Куратор просит пересмотреть ответ. ${question.explanation}`
        : "Этот термин не соответствует условию. Попробуйте ещё раз.";
    elements.missionHint.style.color = "var(--rust)";
    renderStats();
    renderBuilder();
    saveState();
    return;
  }

  if (citadel) state.accessWrongAnswer = null;
  if (dialog) state.insightWrongAnswer = null;
  state.selected[questionIndex] = id;
  elements.missionHint.textContent = citadel
    ? `Ответ принят. ${question.explanation}`
    : `Верно. ${question.explanation}`;
  elements.missionHint.style.color = "var(--moss)";

  if (questionIndex === mission.questions.length - 1) {
    if (citadel) {
      state.accessComplete = true;
      renderBuilder();
    }
    if (dialog) {
      state.insightComplete = true;
      renderBuilder();
    }
    completeMission(mission.maxScore);
    return;
  }

  state.activeSlot += 1;
  renderBuilder();
}

