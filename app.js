async function submitAdminDialog(event) {
  event.preventDefault();

  if (adminDialogMode === "logout") {
    adminActive = false;
    saveAdminSession();
    closeAdminDialog();
    renderAdminState();
    renderBuilder();
    return;
  }

  elements.adminSubmit.disabled = true;
  elements.adminError.hidden = true;

  try {
    const response = await fetch("/api/admin/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ password: elements.adminPassword.value })
    });

    if (!response.ok) {
      elements.adminError.textContent = response.status === 429
        ? "Слишком много попыток. Попробуйте снова через 10 минут."
        : "Неверный пароль. Попробуйте ещё раз.";
      elements.adminError.hidden = false;
      elements.adminPassword.select();
      return;
    }
  } catch {
    elements.adminError.textContent = "Сервер недоступен. Попробуйте ещё раз.";
    elements.adminError.hidden = false;
    elements.adminPassword.select();
    return;
  } finally {
    elements.adminSubmit.disabled = false;
  }

  adminActive = true;
  saveAdminSession();
  closeAdminDialog();
  renderAdminState();
  renderBuilder();
}

