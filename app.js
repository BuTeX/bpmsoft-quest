function renderSceneRoute(mission = getMission()) {
  const steps = mission.sceneSteps || [];
  elements.sceneRoute.innerHTML = "";
  elements.sceneRoute.hidden = steps.length === 0;
  elements.sceneBanner.classList.toggle("is-route-complete", steps.length > 0 && selectedCount() === steps.length);

  if (steps.length === 0) return;

  const svgNamespace = "http://www.w3.org/2000/svg";
  const routeLines = document.createElementNS(svgNamespace, "svg");
  routeLines.setAttribute("class", "scene-route-lines");
  routeLines.setAttribute("viewBox", "0 0 100 100");
  routeLines.setAttribute("preserveAspectRatio", "none");
  routeLines.setAttribute("aria-hidden", "true");

  for (let index = 0; index < steps.length - 1; index += 1) {
    const line = document.createElementNS(svgNamespace, "line");
    line.setAttribute("x1", steps[index].x);
    line.setAttribute("y1", steps[index].y);
    line.setAttribute("x2", steps[index + 1].x);
    line.setAttribute("y2", steps[index + 1].y);
    line.setAttribute("class", `scene-route-line${selectedCount() > index ? " is-lit" : ""}`);
    routeLines.append(line);
  }

  elements.sceneRoute.append(routeLines);

  steps.forEach((step, index) => {
    const selectedId = state.selected[index];
    const selectedTool = selectedId ? getTool(selectedId, mission) : null;
    const active = !selectedTool && index === selectedCount();
    const marker = document.createElement("div");
    marker.className = `scene-route-marker${selectedTool ? " is-filled" : ""}${active ? " is-active" : ""}`;
    marker.style.setProperty("--step-x", `${step.x}%`);
    marker.style.setProperty("--step-y", `${step.y}%`);
    marker.innerHTML = `
      <span>${selectedTool ? "✓" : index + 1}</span>
      <strong>${selectedTool ? selectedTool.name : active ? index === 0 ? "Начните здесь" : "Следующий шаг" : `Шаг ${index + 1}`}</strong>
    `;
    marker.setAttribute(
      "aria-label",
      selectedTool
        ? `Шаг ${index + 1}: выбран термин ${selectedTool.name}`
        : `Шаг ${index + 1}${active ? ", следующий для заполнения" : ""}`
    );
    elements.sceneRoute.append(marker);
  });
}

