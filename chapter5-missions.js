const commonActors = [
  { id: "source", label: "Внешний контур", icon: "source" },
  { id: "bpmsoft", label: "BPMSoft", icon: "hub" },
  { id: "operator", label: "Операционный центр", icon: "operator" },
  { id: "passenger", label: "Пассажир", icon: "passenger" }
];

function choice(id, label, badValue, badLabel, goodValue, goodLabel, goodHelp, { confirmed = false } = {}) {
  return {
    id,
    label,
    type: "single-choice",
    defaultValue: confirmed ? goodValue : badValue,
    confirmed,
    options: [
      { value: badValue, label: badLabel, help: "Это правило воспроизводит исходное расхождение." },
      { value: goodValue, label: goodLabel, help: goodHelp }
    ]
  };
}

function scenarioRound({
  id,
  missionId,
  title,
  briefing,
  events,
  divergence,
  controls,
  successHeadline,
  focus = "Операционный маршрут"
}) {
  const accepted = Object.fromEntries(controls.map((control) => [
    control.id,
    control.options.at(-1).value
  ]));
  const requiredOutcome = `${id.toLowerCase()}-protected`;
  const forbiddenOutcome = `${id.toLowerCase()}-violation`;

  return {
    id,
    missionId,
    title,
    briefing,
    actors: commonActors,
    initialState: {
      visualStatus: "normal",
      focus,
      headline: "Сценарий готов к запуску",
      detail: "Наблюдайте за одним и тем же потоком событий. После исходного прогона вернитесь к первому неверному решению.",
      progressLabel: "Ожидание",
      entities: [
        { id: "source", label: "Источник", value: "готов", state: "normal" },
        { id: "bpmsoft", label: "BPMSoft", value: "ожидает", state: "normal" },
        { id: "operator", label: "Контроль", value: "не выполнен", state: "normal" },
        { id: "passenger", label: "Результат", value: "не сформирован", state: "normal" }
      ]
    },
    ticks: events.map((event, index) => {
      const beforeDivergence = index < divergence;
      const atDivergence = index === divergence;
      const finalTick = index === events.length - 1;
      const baselineLevel = beforeDivergence ? "info" : atDivergence ? "warning" : "error";
      const successLevel = beforeDivergence ? "info" : atDivergence ? "success" : "info";
      const commonPatch = {
        headline: event,
        progressLabel: `${index + 1} / ${events.length}`,
        focus,
        entities: [
          { id: "source", label: "Источник", value: index === 0 ? "событие создано" : "поток активен", state: "normal" },
          { id: "bpmsoft", label: "BPMSoft", value: beforeDivergence ? "обрабатывает" : "решение принято", state: beforeDivergence ? "normal" : "warning" },
          { id: "operator", label: "Контроль", value: beforeDivergence ? "ожидает" : "проверяет правило", state: beforeDivergence ? "normal" : "warning" },
          { id: "passenger", label: "Результат", value: finalTick ? "доставлен" : "формируется", state: finalTick ? "warning" : "normal" }
        ]
      };
      return {
        id: `T${index}`,
        timeLabel: `${String(8 + index).padStart(2, "0")}:${String(index * 7).padStart(2, "0")}`,
        title: event,
        events: [{
          id: `${id}-e${index}`,
          type: "scenario-state",
          actorId: index === 0 ? "source" : "bpmsoft",
          targetId: finalTick ? "passenger" : "operator",
          payload: {
            baseline: {
              patch: {
                ...commonPatch,
                visualStatus: beforeDivergence ? "normal" : "failure",
                detail: beforeDivergence
                  ? "Подтверждённое событие обработано одинаково в обоих прогонах."
                  : atDivergence
                    ? "Здесь система впервые принимает неверное решение. Следующие такты — уже последствия."
                    : "Расхождение распространяется дальше по операционному маршруту.",
                entities: commonPatch.entities.map((entity) => beforeDivergence
                  ? entity
                  : { ...entity, state: entity.id === "source" ? "normal" : "failure" })
              },
              outcomes: atDivergence ? [forbiddenOutcome] : [],
              journal: [{ level: baselineLevel, text: event }]
            },
            success: {
              patch: {
                ...commonPatch,
                visualStatus: beforeDivergence ? "normal" : "recovered",
                headline: finalTick ? successHeadline : event,
                detail: beforeDivergence
                  ? "Поток до контрольной точки совпадает с исходным прогоном."
                  : atDivergence
                    ? "Настроенное правило сработало в первой проблемной точке."
                    : "Контрольный прогон сохраняет согласованное состояние без скрытых побочных эффектов.",
                entities: commonPatch.entities.map((entity) => ({
                  ...entity,
                  value: finalTick && entity.id === "passenger" ? "подтверждён" : entity.value,
                  state: beforeDivergence ? "normal" : "recovered"
                }))
              },
              outcomes: finalTick ? [requiredOutcome] : [],
              journal: [{ level: successLevel, text: finalTick ? successHeadline : event }]
            }
          }
        }],
        journal: [{ level: baselineLevel, text: event }]
      };
    }),
    controls,
    guidance: {
      confirmedControls: controls.filter((control) => control.confirmed).map((control) => control.id)
    },
    solution: {
      firstDivergenceTickId: `T${divergence}`,
      acceptedConfigurations: [accepted],
      requiredOutcomes: [requiredOutcome],
      forbiddenOutcomes: [forbiddenOutcome]
    },
    feedback: {
      passed: `${successHeadline} Первый неверный выбор устранён, а тот же поток событий прошёл без запрещённых последствий.`,
      "checkpoint-error": "Правила настроены верно, но выбран поздний симптом. Вернитесь к первому такту, где система сама приняла неверное решение.",
      "rule-error": "Контрольная точка найдена, но одно или несколько правил всё ещё воспроизводят исходный риск.",
      "mixed-error": "Пересмотрите и момент первого расхождения, и настройки. Следствия нельзя использовать вместо причины."
    },
    presentation: { scene: "operational-twin", focusEntityId: focus }
  };
}

