const c4Decode = (encoded) => {
  const [id, name, note] = encoded.split("|");
  return { id, name, note };
};

const c4Spot = (id, x, y, label, fact) => ({ id, x, y, label, fact });

const c4Card = (role, encoded, correct = false) => ({
  role,
  ...c4Decode(encoded),
  correct
});

const c4Stage = (
  id,
  kicker,
  title,
  instruction,
  hotspots,
  correct,
  decoys,
  successTitle,
  successCopy
) => ({
  id,
  kicker,
  title,
  instruction,
  hotspots,
  cards: [
    c4Card("cause", correct.cause, true),
    c4Card("mechanism", correct.mechanism, true),
    c4Card("test", correct.test, true),
    c4Card("cause", decoys.cause),
    c4Card("mechanism", decoys.mechanism),
    c4Card("test", decoys.test)
  ],
  solution: {
    cause: c4Decode(correct.cause).id,
    mechanism: c4Decode(correct.mechanism).id,
    test: c4Decode(correct.test).id
  },
  successTitle,
  successCopy
});

